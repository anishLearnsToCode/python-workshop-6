a = int(input())
b = int(input())

# addition (+)
print(a + b)

# subtraction (-)
print(a - b)

# multiplication (*)
print(a * b)

# division (/) --> floating
print(a / b)

# power operator (a ^ b) ^ --> **
print(a ** b)

# integer division (//) --> int
# a // b = int(a / b)

# Modulus Operator (%) --> remainder
print(a % b)
